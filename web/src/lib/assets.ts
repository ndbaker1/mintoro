const sampleSize = <T>([...arr]: T[], n = 1): T[] => shuffle(arr).slice(0, n);

// https://www.30secondsofcode.org/js/s/array-sample-shuffle-weighted-selection/
function shuffle<T>([...arr]: T[]): T[] {
	let m = arr.length;
	while (m) {
		const i = Math.floor(Math.random() * m--);
		[arr[m], arr[i]] = [arr[i], arr[m]];
	}
	return arr;
}
